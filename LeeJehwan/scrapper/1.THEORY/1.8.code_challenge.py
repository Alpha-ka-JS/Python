def plus(a, b):
    return int(a) + int(b)


def minus(a, b):
    return int(a) - int(b)


def times(a, b):
    return int(a) * int(b)


def division(a, b):
    return int(a) / int(b)


def negation(a):
    return -1 * int(a)


def power(a, b):
    return int(a) ** int(b)


def reminder(a, b):
    return int(a) % int(b)


print(plus(3, 6))
print(minus(9, 2))
print(times(3, 6))
print(division(81, 3))
print(negation(444))
print(power(6, 2))
print(reminder(11, 3))
